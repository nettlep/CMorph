/*±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±±*/

/* Straight:
 *
 * Compute a straight-line spline passing through the len1 points in arrays
 * x1, y1, where y1 = f(x1)
 * len2 positions on the line are to be computed. Their positions are
 * given in x2. The line values are stored in y2.
 */

int   Straight(float *x1, float *y1, int len1, float *x2, float *y2, int len2)
{
int      i, j;

   // GO THROUGH THE MESH POINTS CALCULATING THE PROPER y2 VALUES
   for (i = 0; i < len2; i++ )
   {
      // VISIT THE x1 VALUES TO FIND WHERE THE POINT LIES IN THE MESH
      for (j = 1; j < len1; j++)
      {
         if (x2[i] <= x1[j])
         {
            y2[i] = (x2[i] - x1[j-1]) / (x1[j] - x1[j-1]) * (y1[j] - y1[j-1]) + y1[j-1];
            break;
         }
      }

      // HANDLE A MESH POINT THAT IS BEYOND THE RANGE GIVEN
      if (j == len1)
         return( E_CORRUPTMESH );
   }

   return( E_NONE );
}

